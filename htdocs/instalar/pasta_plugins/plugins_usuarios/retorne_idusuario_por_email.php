<?php


// retorna o id de usuario por email --------------------------------------------------------------------------------

function retorne_idusuario_por_email($email){


// global -----------------------------------------------------------------------------------------------------------

global $tabela_banco; // tabela

// ------------------------------------------------------------------------------------------------------------------




// se email for nulo retorne nulo -----------------------------------------------------------------------------------

if($email == null){

return null; // retorno nulo

};

// ------------------------------------------------------------------------------------------------------------------




// tabela usando ----------------------------------------------------------------------------------------------------

$tabela_banco_usando = $tabela_banco[1]; // tabela usando

// ------------------------------------------------------------------------------------------------------------------




// query ------------------------------------------------------------------------------------------------------------

$query = "select *from $tabela_banco_usando where email='$email';"; // query

// ------------------------------------------------------------------------------------------------------------------




// comando ----------------------------------------------------------------------------------------------------------

$comando = comando_executa($query); // comando

// ------------------------------------------------------------------------------------------------------------------




// numero de linhas -------------------------------------------------------------------------------------------------

$numero_linhas = mysql_num_rows($comando); // numero de linhas

// ------------------------------------------------------------------------------------------------------------------




// dados ------------------------------------------------------------------------------------------------------------

if($numero_linhas > 0){




// dados ------------------------------------------------------------------------------------------------------------

$dados = mysql_fetch_array($comando, MYSQL_ASSOC); // dados

// ------------------------------------------------------------------------------------------------------------------




// retorno ----------------------------------------------------------------------------------------------------------

return $dados['idusuario']; // retorno

// ------------------------------------------------------------------------------------------------------------------




}else{




// retorno nulo -----------------------------------------------------------------------------------------------------

return null; // retorno nulo

// ------------------------------------------------------------------------------------------------------------------




};

// ------------------------------------------------------------------------------------------------------------------




};

// ------------------------------------------------------------------------------------------------------------------


?>